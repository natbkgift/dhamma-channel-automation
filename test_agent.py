def test_topic_prioritizer(input_data=None):
    """‡∏ó‡∏î‡∏™‡∏≠‡∏ö Topic Prioritizer Agent - ‡∏à‡∏±‡∏î‡∏•‡∏≥‡∏î‡∏±‡∏ö‡∏´‡∏±‡∏ß‡∏Ç‡πâ‡∏≠"""
    print_header("Testing: Topic Prioritizer Agent")
    
    # ‡∏ñ‡πâ‡∏≤‡πÑ‡∏°‡πà‡∏°‡∏µ input ‡πÉ‡∏´‡πâ‡∏£‡∏±‡∏ô Trend Scout ‡∏Å‡πà‡∏≠‡∏ô
    if input_data is None:
        print_info("‡πÑ‡∏°‡πà‡∏°‡∏µ input, ‡∏£‡∏±‡∏ô Trend Scout ‡∏Å‡πà‡∏≠‡∏ô...")
        trend_result = test_trend_scout()
        input_data = {
            "candidates": trend_result["trends"]
        }
    
    candidates = input_data.get("candidates", [])
    print_info(f"‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏à‡∏±‡∏î‡∏•‡∏≥‡∏î‡∏±‡∏ö {len(candidates)} ‡∏´‡∏±‡∏ß‡∏Ç‡πâ‡∏≠...")
    
    # ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì priority score
    prioritized = []
    for topic in candidates:
        # ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏à‡∏≤‡∏Å‡∏´‡∏•‡∏≤‡∏¢‡∏õ‡∏±‡∏à‡∏à‡∏±‡∏¢
        base_score = topic.get("score", 0)
        difficulty_factor = {"‡∏á‡πà‡∏≤‡∏¢": 1.2, "‡∏Å‡∏•‡∏≤‡∏á": 1.0, "‡∏Å‡∏•‡∏≤‡∏á-‡∏¢‡∏≤‡∏Å": 0.8, "‡∏¢‡∏≤‡∏Å": 0.6}
        risk_factor = {"‡∏ï‡πà‡∏≥": 1.2, "‡∏Å‡∏•‡∏≤‡∏á": 1.0, "‡∏™‡∏π‡∏á": 0.7}
        
        diff_mult = difficulty_factor.get(topic.get("difficulty", "‡∏Å‡∏•‡∏≤‡∏á"), 1.0)
        risk_mult = risk_factor.get(topic.get("risk", "‡∏Å‡∏•‡∏≤‡∏á"), 1.0)
        
        priority_score = base_score * diff_mult * risk_mult
        
        prioritized.append({
            "rank": 0,  # ‡∏à‡∏∞‡∏Å‡∏≥‡∏´‡∏ô‡∏î‡∏ó‡∏µ‡∏´‡∏•‡∏±‡∏á
            "title": topic["title"],
            "priority_score": round(priority_score, 2),
            "original_score": base_score,
            "reasons": [
                f"Trend score: {base_score}/100",
                f"Difficulty: {topic.get('difficulty', 'N/A')} (√ó{diff_mult})",
                f"Risk: {topic.get('risk', 'N/A')} (√ó{risk_mult})",
                topic.get("why_now", "")
            ],
            "recommendation": "‡πÅ‡∏ô‡∏∞‡∏ô‡∏≥‡∏ó‡∏≥" if priority_score >= 85 else "‡∏û‡∏¥‡∏à‡∏≤‡∏£‡∏ì‡∏≤" if priority_score >= 70 else "‡∏£‡∏≠‡∏Å‡πà‡∏≠‡∏ô",
            "estimated_effort": "‡∏ï‡πà‡∏≥" if topic.get("difficulty") == "‡∏á‡πà‡∏≤‡∏¢" else "‡∏Å‡∏•‡∏≤‡∏á" if topic.get("difficulty") == "‡∏Å‡∏•‡∏≤‡∏á" else "‡∏™‡∏π‡∏á"
        })
    
    # ‡πÄ‡∏£‡∏µ‡∏¢‡∏á‡∏ï‡∏≤‡∏° priority_score
    prioritized.sort(key=lambda x: x["priority_score"], reverse=True)
    
    # ‡∏Å‡∏≥‡∏´‡∏ô‡∏î rank
    for i, item in enumerate(prioritized, 1):
        item["rank"] = i
    
    result = {
        "agent": "TopicPrioritizer",
        "version": "1.0",
        "executed_at": datetime.now().isoformat(),
        "total_topics": len(prioritized),
        "recommended": len([p for p in prioritized if p["recommendation"] == "‡πÅ‡∏ô‡∏∞‡∏ô‡∏≥‡∏ó‡∏≥"]),
        "prioritized_topics": prioritized,
        "metadata": {
            "criteria": ["trend_score", "difficulty", "risk", "audience_fit"],
            "processing_time": "0.5 seconds"
        }
    }
    
    # ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏ú‡∏•
    output_file = ROOT / "output" / "test_agents" / f"topic_prioritizer_{datetime.now().strftime('%Y%m%d_%H%M%S')}.json"
    write_json(output_file, result)
    
    print_success(f"‡∏à‡∏±‡∏î‡∏•‡∏≥‡∏î‡∏±‡∏ö {len(prioritized)} ‡∏´‡∏±‡∏ß‡∏Ç‡πâ‡∏≠‡πÄ‡∏£‡∏µ‡∏¢‡∏ö‡∏£‡πâ‡∏≠‡∏¢")
    print(f"\n‡πÅ‡∏ô‡∏∞‡∏ô‡∏≥‡∏ó‡∏≥: {result['recommended']} ‡∏´‡∏±‡∏ß‡∏Ç‡πâ‡∏≠\n")
    
    print("Top 3 Recommended Topics:")
    for topic in prioritized[:3]:
        print(f"\n#{topic['rank']}. {topic['title']}")
        print(f"   Priority Score: {topic['priority_score']}/100")
        print(f"   ‡πÅ‡∏ô‡∏∞‡∏ô‡∏≥: {topic['recommendation']}")
        print(f"   ‡∏Ñ‡∏ß‡∏≤‡∏°‡∏û‡∏¢‡∏≤‡∏¢‡∏≤‡∏°: {topic['estimated_effort']}")
    
